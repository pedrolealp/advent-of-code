    public function getPartOne()
    {
        return $this->getTress($this->input, 3, 1);
    }

    public function gerPartTwo()
    {
        $slopes = [
            [1, 1],
            [3, 1],
            [5, 1],
            [7, 1],
            [1, 2],
        ];

        $result = 1;

        foreach ($slopes as $slope) {
            $result *= $this->getTress($this->input, $slope[0], $slope[1]);
        }

        return $result;
    }

    public function getTress($input, $right, $down)
    {
        $lines = explode(PHP_EOL, $input);

        $length = strlen($lines[0]);

        $numberOfTress = 0;

        $x = $right;

        for ($y = 1; $y < count($lines); $y = $y + $down) {
            if (mb_str_split($lines[$y])[$x] === '#') {
                $numberOfTress++;
            }

            $x = ($x + $right) % $length;
        }

        return $numberOfTress;
    }
